// the host wants to read data
// make at least the minimum of a usb packet and nBlock*blocksize of data available in the transfer buffer
// and set the address and the total transfer length accordingly
void usb_handle_read10_request(UsbMultipacketTransfer *  transfer, uint32_t blockStart, uint16_t nBlocks)
{
    transfer->address=(uint32_t)msc_disk[blockStart];
    transfer->len=nBlocks*LOGICAL_BLOCK_SIZE;
}

// prepare for writing a least the minimum of a usb packet and nBlock*blocksize bytes
// and set the address and the total transfer length accordingly
void usb_handle_write10_request(UsbMultipacketTransfer *  transfer, uint32_t blockStart, uint16_t nBlocks)
{
    transfer->address=(uint32_t)msc_disk[blockStart];
    transfer->len=nBlocks*LOGICAL_BLOCK_SIZE;
}

